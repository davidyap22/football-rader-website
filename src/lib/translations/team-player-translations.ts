/**
 * Get localized team name with fallback
 */
export function getTeamNameTranslation(
  teamName: string,
  locale: string
): string {
  const localeKey = locale === 'zh' ? 'zh_cn' : locale === 'tw' ? 'zh_tw' : locale as keyof TeamTranslation;
  const translation = TEAM_TRANSLATIONS[teamName];

  if (!translation) return teamName;

  const localizedName = translation[localeKey as keyof TeamTranslation];
  return (typeof localizedName === 'string' ? localizedName : undefined) || translation.en || teamName;
}

/**
 * Get localized player name with fallback
 */
export function getPlayerNameTranslation(
  playerName: string,
  locale: string
): string {
  const localeKey = locale === 'zh' ? 'zh_cn' : locale === 'tw' ? 'zh_tw' : locale as keyof PlayerTranslation;
  const translation = PLAYER_TRANSLATIONS[playerName];

  if (!translation) return playerName;

  const localizedName = translation[localeKey as keyof PlayerTranslation];
  return (typeof localizedName === 'string' ? localizedName : undefined) || translation.en || playerName;
}

/**
 * Check if team has translation available
 */
export function hasTeamTranslation(teamName: string): boolean {
  return teamName in TEAM_TRANSLATIONS;
}

/**
 * Check if player has translation available
 */
export function hasPlayerTranslation(playerName: string): boolean {
  return playerName in PLAYER_TRANSLATIONS;
}

/**
 * Get all translated team names for a specific locale
 */
export function getAllTeamNamesForLocale(locale: string): Record<string, string> {
  const localeKey = locale === 'zh' ? 'zh_cn' : locale === 'tw' ? 'zh_tw' : locale as keyof TeamTranslation;
  const result: Record<string, string> = {};

  for (const [englishName, translations] of Object.entries(TEAM_TRANSLATIONS)) {
    const localizedName = translations[localeKey as keyof TeamTranslation];
    result[englishName] = (typeof localizedName === 'string' ? localizedName : undefined) || translations.en || englishName;
  }

  return result;
}

